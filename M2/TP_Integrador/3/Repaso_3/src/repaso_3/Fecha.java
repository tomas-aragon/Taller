package repaso_3;


public class Fecha {

private String ciudad;
private String d;

    public Fecha(String ciudad, String d) {
        this.ciudad = ciudad;
        this.d = d;
    }




    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getDia() {
        return d;
    }

    public void setDia(String d) {
        this.d = d;
    }


    
}
